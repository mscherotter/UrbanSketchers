namespace UrbanSketchers.Controls
{
    /// <summary>
    ///     the bitmap file format
    /// </summary>
    public enum DrawingFileFormat
    {
        /// <summary>
        ///     Windows Bitmap format
        /// </summary>
        Bmp,

        /// <summary>
        ///     Portable Network Graphics format
        /// </summary>
        Png,

        /// <summary>
        ///     JPEG format
        /// </summary>
        Jpeg,

        /// <summary>
        ///     TIFF format
        /// </summary>
        Tiff,

        /// <summary>
        ///     GIF format
        /// </summary>
        Gif,

        /// <summary>
        ///     JPEG XR Format
        /// </summary>
        JpegXr,

        /// <summary>
        ///     Ink serialization format
        /// </summary>
        Isf,

        /// <summary>
        ///     Gif with embedded ink serialization format
        /// </summary>
        GifWithEmbeddedIsf
    }
}